package demo;

public class Calculator {

    public int plus(int a, int b) {
        return a+b;
    }

    public int minus(int a, int b) {
        return a-b;
    }

    public int mul(int a, int b) {
        return a*b;
    }

    public double div(int a, int b) throws Exception {
        return (double) a /b;
    }

}
